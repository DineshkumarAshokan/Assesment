package com.prodapt.basics;

public class SimpleInterest {
	
		
   static int p;
   static  int r;
   static int t;
   static int Amount;
   static int SI;
   
   static void SI()
   {
   SI=(p*r*t)/100;
   System.out.println("Simple Interest="+SI);
   Amount=SI+p;
   System.out.println("Total Amount="+Amount);
   
   
   
     
   
	}

}
